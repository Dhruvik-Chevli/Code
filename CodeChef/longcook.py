def isleap(year):
    if year%400==0:
        return True
    if year%100==0:
        return False
    if year%4==0:
        return True
    return False
def countSatSun(m1,m2,y1,y2,year):
    count=0
    for i in range(y1,y2+1):
        if m1>2 and i==y1:
            continue
        if m2<2 and i==y2:
            continue
        if isleap(i):
            if year[i%400]==6:
                count+=1
        else:
            if year[i%400]==6 or year[i%400]==0:
                count+=1
    return count
